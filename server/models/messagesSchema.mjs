import mongoose from 'mongoose';

const MessageSchema = new mongoose.Schema({
    fName: {
        type: String,
        required: true
    },
    lName: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    subject: {
        type: String,
        required: true
    },
    message: {
        type: String,
        required: true
    }
});

const Messages = mongoose.model('Messages', MessageSchema, 'messages');

export default Messages;